#DataType
a = 1
b = 2.2
c = "hello"
d = True
e = [1,2,3,4]
f = (1,2,3,4)
g = {1,2,3,4}
h = {"name": "John", "age": 20}

print(type(a)) #int
print(type(b)) #float
print(type(c)) #string
print(type(d)) #boolean
print(type(e)) #list
print(type(f)) #tuple
print(type(g)) #set
print(type(h)) #dictionary


#Operators
print(a + b) #addition
print(a * b) #multiplication
print(a / b) #division
print(a % b) #modulus
print(a // b) #floor division
print(a ** b) #power

#comparison operators
print(a == b) #equal to
print(a != b) #not equal to
print(a > b) #greater than
print(a < b) #less than
print(a >= b) #greater than or equal to
print(a <= b) #less than or equal to
